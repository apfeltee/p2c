def processfile(file)
  data = File.read(file)
  VARNAMES.each do |var, sub|
    rx = Regexp.new('\b' + var + '\b')
    data.gsub!(rx, sub)
  end
  File.write(file, data)
end

begin
  ARGV.each do |arg|
    processfile(arg)
  end
end
